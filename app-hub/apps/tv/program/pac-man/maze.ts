export const createMaze = (): Maze => {
  const rows = RAW_MAP.length
  const cols = RAW_MAP[0].length
  const tiles = RAW_MAP.flat()
  return { rows, cols, tiles }
}

export const isWall = (maze: Maze, x: number, y: number): boolean => {
  if (x < 0 || y < 0 || x >= maze.cols || y >= maze.rows) return true
  return maze.tiles[y * maze.cols + x] === 1
}

export const isTunnel = (maze: Maze, x: number, y: number): boolean => {
  if (x < 0 || y < 0 || x >= maze.cols || y >= maze.rows) return false
  return maze.tiles[y * maze.cols + x] === 0 && (x === 0 || x === maze.cols - 1)
}

export const isGhostHouse = (maze: Maze, x: number, y: number): boolean => {
  if (x < 0 || y < 0 || x >= maze.cols || y >= maze.rows) return false
  return maze.tiles[y * maze.cols + x] === 2
}
